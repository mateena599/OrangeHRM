package com.BasicJava;

public class InitializingExample {

	public static void main(String[] args) 
	{
		int a=10;
		
		a=20;
		
		char b='a';
		
		String string="SELENIUM";
		
		string=string.concat(" -----------selenium is one of the Testing Tool");
		
		double d=12.5255;
		
		float h=25;
		
		
//EXECUTABLE STATEMENTS
		System.out.println(string);
		
		System.out.println("The value of an INTEGER   a is  : "+a);
		
		System.out.println("The value of a  CHARACTER b is  : "+b);

		System.out.println("The value of a  DOUBLE    d is  : "+d);
		
		System.out.println("The value of a  FLOAT     h is  : "+h);
		 
	}

}
